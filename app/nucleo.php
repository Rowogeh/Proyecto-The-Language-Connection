		<div class="text-center">Misión</div>

		<p>
		Tiene como fin fomentar a través de la música el espíritu e intelecto de los niños y jóvenes para robustecer en ellos un criterio amplio con sentido de organización y responsabilidad, valores que se logran por medio de las exigencias de la disciplina musical.  
		</p>

		<div class="text-center">Objetivos de la Comunidad</div>

		<li>Generar mecanismos de sensibilización y difusión de la música orquestal en las comunidades, con el propósito de crear medios de acercamiento entre la Orquesta y el contexto social.</li>
		<li>Impartir estudios académicos de la música orquestal a través de un sistema educativo formal dirigido a niños y jóvenes.</li>
		<li>Estimular la creación de grupos de cámara para apoyar las inquietudes musicales de los jóvenes de estudios avanzados de la Orquesta.</li>
		<li>Fortalecer el conocimiento y ejecución de expresiones musicales del país como medio para consolidar los valores de la identidad cultural venezolana.</li>
		<li>Promover el conocimiento y deleite de la música académica universal como medio de expresión artística entre los pueblos.</li>
		
