		<p>
		La Fundación Orquesta Sinfónica Juvenil e Infantil de Tovar tiene como visión final, Lograr un ciudadano participativo, responsable y proactivo, con un alto nivel de competitividad que lo conduzca a sobresalir ante las  
		exigencias profesionales, los deberes sociales y el crecimiento moral del país.
